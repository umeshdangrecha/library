public class MyMath {
    public static int sum(int a,int b){
        return a+b;
    }
    public static int sub(int a,int b){
        return a-b;
    }
    public static int multiply(int a,int b){
        return a*b;
    }
    public static float div(int a,int b){
        return b==0?0:a/b;
    }
}
